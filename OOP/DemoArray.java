
public class DemoArray {
    public static void main(String a[]) {
        int nums[] = { 3, 7, 6, 8 };
        int nums1[] = new int[4];
        nums1[0] = 3;
        nums1[1] = 5;

        for (int i = 0; i < 4; i++) {
            System.out.println(nums[i]);
            System.out.println(nums1[i]);
        }
    }

}
